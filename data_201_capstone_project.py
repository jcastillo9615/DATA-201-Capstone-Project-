plt.xlabel("Insulin group")
plt.ylabel("Average days in hospital")
plt.show()

print("Interpretation of Graph 1:")
print("This bar chart compares the average length of hospital stay between patients who received insulin and those who did not. "
      "The higher average for the insulin group suggests that insulin use is associated with longer hospital stays.")
print()

