# -------------------------
# Statistical Analysis: Independent t-test
# -------------------------
from scipy.stats import ttest_ind

# Separate the two groups
with_insulin = df[df['insulin_group'] == 'With Insulin']['time_in_hospital']
no_insulin = df[df['insulin_group'] == 'No Insulin']['time_in_hospital']

# Perform Welch's t-test
t_stat, p_value = ttest_ind(with_insulin, no_insulin, equal_var=False)

print("T-test Results")
print("t-statistic:", round(t_stat, 3))
print("p-value:", p_value)

if p_value < 0.05:
    print("There is a statistically significant difference in hospital length of stay between patients who use insulin and those who do not.")
else:
    print("There is no statistically significant difference in hospital length of stay between the two groups.")

# -------------------------
# Statistical Analysis: Correlation between age and hospital stay
# -------------------------

correlation = df['age_mid'].corr(df['time_in_hospital'])

print("Correlation between age and time in hospital:", round(correlation, 3))

if correlation > 0:
    print("Older patients tend to stay longer in the hospital.")
elif correlation < 0:
    print("Older patients tend to stay fewer days in the hospital.")
else:
    print("There is no relationship between age and time in hospital.")

