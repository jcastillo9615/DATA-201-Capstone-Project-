# How Insulin Use Affects Hospital Length of Stay

import pandas as pd
import matplotlib.pyplot as plt

# -------------------------
# Motivation
# -------------------------
# Hospital length of stay is a key driver of healthcare cost and patient outcomes.
# This project explores whether insulin use is associated with longer hospital stays.

url = "https://raw.githubusercontent.com/niteen11/DataAnalyticsAcademy/master/Python/dataset_diabetes/diabetic_data.csv"
df = pd.read_csv(url)

df.head()

# -------------------------
# Data Cleaning & Transformations
# -------------------------

# Keep valid genders
df = df[df['gender'].isin(['Male', 'Female'])].copy()

# Convert to numeric
df['time_in_hospital'] = pd.to_numeric(df['time_in_hospital'], errors='coerce')

# Drop missing values
df = df.dropna(subset=['time_in_hospital', 'insulin', 'age', 'num_medications', 'number_diagnoses'])

# Create insulin group
df = df[df['insulin'].isin(['Up','Down','Steady','No'])].copy()
df['insulin_group'] = df['insulin'].apply(lambda x: 'With Insulin' if x != 'No' else 'No Insulin')

# Convert age ranges to numeric midpoints
def age_mid(age_str):
    lo_hi = age_str.strip('[]()').replace(')', '').split('-')
    lo = int(lo_hi[0])
    hi = int(lo_hi[1])
    return (lo + hi) / 2

df['age_mid'] = df['age'].apply(age_mid)

df[['age','age_mid','insulin','insulin_group','time_in_hospital']].head()

# -------------------------
# Descriptive Statistics
# -------------------------
df['time_in_hospital'].describe()

# -------------------------
# Statistical Analysis: Correlation
# -------------------------
features = ['num_medications', 'num_lab_procedures', 'number_diagnoses']
corrs = df[features + ['time_in_hospital']].corr(numeric_only=True)['time_in_hospital']
corrs

